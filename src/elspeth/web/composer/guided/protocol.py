def validate_current_turn(step: GuidedStep, turn: Mapping[str, Any]) -> TurnType:
    """Validate one exact current-schema turn and return its discriminator.

    Construction, durable load, and replay share this custody boundary. Those
    callers translate ``ValueError`` into the error class appropriate to the
    trust level of the turn they are handling.
    """

    if type(step) is not GuidedStep:
        raise ValueError("current-schema turn step must be GuidedStep")
    if not isinstance(turn, Mapping) or set(turn) != {"type", "step_index", "payload"}:
        raise ValueError("current-schema turn must have exactly type, step_index, and payload")
    raw_type = turn["type"]
    if type(raw_type) is not str:
        raise ValueError("current-schema turn type must be a string discriminator")
    try:
        turn_type = TurnType(raw_type)
    except ValueError as exc:
        raise ValueError("current-schema turn type is outside the closed vocabulary") from exc
    step_index = turn["step_index"]
    if type(step_index) is not int or step_index != _STEP_INDEX[step]:
        raise ValueError("current-schema turn step_index does not match its step")
    if turn_type not in legal_turn_types_for(step):
        raise ValueError("current-schema turn type is illegal for its step")
    payload = turn["payload"]
    if not isinstance(payload, Mapping):
        raise ValueError("current-schema turn payload must be a mapping")
    if error := validate_payload(turn_type, payload):
        raise ValueError(f"current-schema turn payload is invalid: {error}")
    return turn_type


